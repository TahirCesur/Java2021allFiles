package JavaKonusalSorular.Pratik04_ArtirmaAzaltmaOperatorleri;

public class Pr01 {

    public static void main(String[] args) {

        /* Aritmetik atama seklinde su islemleri yapiniz...

        int sayi=20;

        sayi*=5;
        sayi+=5;
        sayi-=5;
        sayi/=5;
        sayi%=5;

        */

        int sayi=20;

        sayi*=5;
        System.out.println("Carpma : " + sayi); // Carpma : 100
        sayi+=5;
        System.out.println("Toplama : " + sayi); // Toplama : 105
        sayi-=5;
        System.out.println("Cıkarma : " + sayi); // Cıkarma : 100
        sayi/=5;
        System.out.println("Bolme : " + sayi); // Bolme : 20
        sayi%=5;
        System.out.println("Modulus : " + sayi); // Modulus : 0






    }
}
