package JavaKonusalSorular.Pratik02_PrimitiveDataTurleri;

public class Pr01 {

    public static void main(String[] args) {

        /*
        Primitive data turlerine deger verip consol da yazdiriniz...

        byte
        short
        int
        long
        float
        double
        boolean
        char

        */

        byte sayi1 = 1;
        short sayi2 = 5;
        int sayi3 = 100;
        long sayi4 = 12345869;
        float sayi5 = 25.4f;
        double sayi6 = 894;
        boolean dogruMu = true;
        char karakter = 'a';

        System.out.println(sayi1); // 1
        System.out.println(sayi2); // 5
        System.out.println(sayi3); // 100
        System.out.println(sayi4); // 12345869
        System.out.println(sayi5); // 25.4
        System.out.println(sayi6); // 894.0
        System.out.println(dogruMu); // true
        System.out.println(karakter); // a

    }
}
