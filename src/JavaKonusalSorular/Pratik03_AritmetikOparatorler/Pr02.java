package JavaKonusalSorular.Pratik03_AritmetikOparatorler;

public class Pr02 {

    public static void main(String[] args) {

        // 10 sayisinin karesini ve kupunu hesaplayan bir program yaziniz...

        int sayi=10;

        System.out.println("Karesi : " + (sayi*sayi)); // Karesi : 100
        System.out.println("Kupu : " + (sayi*sayi*sayi)); // Kupu : 1000
    }
}
