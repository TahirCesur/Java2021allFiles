package JavaKonusalSorular.Pratik05_MantiksalveiliskiselOpetaorler;

public class Pr01 {

    public static void main(String[] args) {

        /* iliskisel Operator yardımıyla su islem sonuclarini yazdiriniz...

        int sayi=100;
        int sayi2=90;

        >buyuk
        <kucuk
        >=buyuk esit
        <=kucuk esit
        == esit
        != esit degil

         */

        int sayi1=100;
        int sayi2=90;

        System.out.println(sayi1>sayi2); // true
        System.out.println(sayi1<sayi2); // false
        System.out.println(sayi1>=sayi2); // true
        System.out.println(sayi1<=sayi2); // false
        System.out.println(sayi1==sayi2); // false
        System.out.println(sayi1!=sayi2); // true


    }
}
